    /// <summary>CP56Time2a格式(7字节)转DateTime</summary>
    public static DateTime CP56Time2aToDateTime(byte[] bytes, int offset = 0)
    {
        int ms = BitConverter.ToUInt16(bytes, offset);
        int minute = bytes[offset + 2] & 0x3F;
        int hour = bytes[offset + 3] & 0x1F;
        int day = bytes[offset + 4] & 0x1F;
        int month = bytes[offset + 5] & 0x0F;
        int year = bytes[offset + 6] & 0x7F;

        year += 2000;
        // 确保有效
        if (month < 1) month = 1;
        if (month > 12) month = 12;
        if (day < 1) day = 1;
        if (day > 31) day = 28;

        try
        {
            return new DateTime(year, month, day, hour, minute, 0).AddMilliseconds(ms);
        }
        catch
        {
            return DateTime.MinValue;
        }
    }

    /// <summary>DateTime转CP56Time2a格式(7字节)</summary>
    public static byte[] DateTimeToCP56Time2a(DateTime dt)
    {
        var result = new byte[7];
        ushort ms = (ushort)(dt.Second * 1000 + dt.Millisecond);
        BitConverter.GetBytes(ms).CopyTo(result, 0);
        result[2] = (byte)(dt.Minute & 0x3F);
        result[3] = (byte)(dt.Hour & 0x1F);
        result[4] = (byte)(dt.Day & 0x1F);
        result[5] = (byte)(dt.Month & 0x0F);
        result[6] = (byte)((dt.Year - 2000) & 0x7F);
        return result;
    }

