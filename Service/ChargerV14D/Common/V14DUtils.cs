    #region SeqNo

    private static ushort _seqNo = 0;
    private static readonly object _seqLock = new();

    /// <summary>获取下一个报文序列号 (1-65535循环)</summary>
    public static ushort NextSeqNo()
    {
        lock (_seqLock)
        {
            _seqNo++;
            if (_seqNo == 0) _seqNo = 1;
            return _seqNo;
        }
    }

