    #region BCD

    /// <summary>BCD字节数组转字符串 (高位在前)</summary>
    public static string BcdToString(byte[] bcd, int offset, int length)
    {
        var sb = new StringBuilder();
        for (int i = offset; i < offset + length; i++)
        {
            sb.Append((bcd[i] >> 4).ToString());
            sb.Append((bcd[i] & 0x0F).ToString());
        }
        return sb.ToString().TrimStart('0');
    }

    /// <summary>字符串转BCD字节数组 (高位在前, 补0)</summary>
    public static byte[] StringToBcd(string s, int length)
    {
        var result = new byte[length];
        // 左侧补0
        s = s.PadLeft(length * 2, '0');
        for (int i = 0; i < length && i * 2 < s.Length; i++)
        {
            byte hi = (byte)(s[i * 2] >= '0' && s[i * 2] <= '9' ? s[i * 2] - '0' : 0);
            byte lo = (byte)(s[i * 2 + 1] >= '0' && s[i * 2 + 1] <= '9' ? s[i * 2 + 1] - '0' : 0);
            result[i] = (byte)((hi << 4) | lo);
        }
        return result;
    }

