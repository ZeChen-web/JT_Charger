    #region ASCII Helpers

    /// <summary>读取定长ASCII字符串</summary>
    public static string ReadAscii(byte[] data, int offset, int length)
    {
        var sb = new StringBuilder();
        for (int i = offset; i < offset + length && i < data.Length; i++)
        {
            if (data[i] == 0) break;
            sb.Append((char)data[i]);
        }
        return sb.ToString();
    }

    /// <summary>写入定长ASCII字符串</summary>
    public static void WriteAscii(string s, byte[] data, int offset, int length)
    {
        var bytes = Encoding.ASCII.GetBytes(s ?? "");
        for (int i = 0; i < length; i++)
        {
            data[offset + i] = (byte)(i < bytes.Length ? bytes[i] : 0);
        }
    }

